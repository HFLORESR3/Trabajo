    void setAccion(String accion) {
    this.accion = accion;
    String titulo = accion + " CLIENTE";
    this.setTitle(titulo);
    habilitarControles();
    
  }
    void setBean(Cliente bean) {
    this.bean = bean;
    cargarDatos();
  }
    private void cargarDatos() {
    if(bean.getId()==0){
    txtCodigo.setText("NONE");
    }else{txtCodigo.setText(Integer.toString(bean.getId()));}
    txtNombre.setText(bean.getNombre());
    txtDireccion.setText(bean.getDireccion());
  }
    private void habilitarControles() {
    boolean editable = true;
    if(accion.equals("Eliminar")){
      editable = false;
    }
    txtNombre.setEditable(editable);
    txtDireccion.setEditable(editable);
  }
